package com.cqupt.project.model;

public class Supervisor {
    private String platform;//平台方

    private String plaCettificate;//平台方证书

    private Long plaRegistNum;//平台方注册编号

    private String plaPhoneNum;//平台方联系电话
    
    private String supervisorIntro;//平台方的公司简介
    
    private String ad;//广告详情

    public String getSupervisorIntro(){
		return supervisorIntro;
	}

	public void setSupervisorIntro(String supervisorIntro) {
		this.supervisorIntro = supervisorIntro;
	}

	public String getAd() {
		return ad;
	}

	public void setAd(String ad) {
		this.ad = ad;
	}

	public String getPlatform() {
        return platform;
    }

    public void setPlatform(String platform) {
        this.platform = platform;
    }

    public String getPlaCettificate() {
        return plaCettificate;
    }

    public void setPlaCettificate(String plaCettificate) {
        this.plaCettificate = plaCettificate;
    }

    public Long getPlaRegistNum() {
        return plaRegistNum;
    }

    public void setPlaRegistNum(long plaRegistNum) {
        this.plaRegistNum = plaRegistNum;
    }

    public String getPlaPhoneNum() {
        return plaPhoneNum;
    }

    public void setPlaPhoneNum(String plaPhoneNum) {
        this.plaPhoneNum = plaPhoneNum;
    }
}