package com.cqupt.project.model;

import java.util.Date;

public class Bid {
	
    private Integer bidId;//标书主键

    private Integer proId;//外键，工程主键

    private String bidDetail;//外键，标书详情

    private Date submitDate;//外键，提交时间
    
    private Company company;
    
    private Integer companyId;

    private String fileId;

    public Integer getBidId() {
        return bidId;
    }

    public void setBidId(Integer bidId) {
        this.bidId = bidId;
    }

    public Integer getProId() {
        return proId;
    }

    public void setProId(Integer proId) {
        this.proId = proId;
    }

    public String getBidDetail() {
        return bidDetail;
    }

    public void setBidDetail(String bidDetail) {
        this.bidDetail = bidDetail;
    }

    public Date getSubmitDate() {
        return submitDate;
    }

    public void setSubmitDate(Date submitDate) {
        this.submitDate = submitDate;
    }

    public Integer getCompanyId() {
        return companyId;
    }

    public void setCompanyId(Integer companyId) {
        this.companyId = companyId;
    }

    public String getFileId() {
        return fileId;
    }

    public void setFileId(String fileId) {
        this.fileId = fileId;
    }

	public Company getCompany() {
		return company;
	}

	public void setCompany(Company company) {
		this.company = company;
	}
}